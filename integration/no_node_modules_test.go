package integration_test

import (
	"path/filepath"
	"testing"

	"github.com/cloudfoundry/dagger"
	"github.com/sclevine/spec"

	. "github.com/onsi/gomega"
)

func testNoNodeModules(t *testing.T, context spec.G, it spec.S) {
	var (
		Expect = NewWithT(t).Expect
		app    *dagger.App
	)

	it.After(func() {
		Expect(app.Destroy()).To(Succeed())
	})

	context("when there are no node modules", func() {
		it.Pend("builds a working OCI image for an app without dependencies", func() {
			var err error
			app, err = dagger.NewPack(
				filepath.Join("testdata", "no_node_modules"),
				dagger.RandomImage(),
				dagger.SetBuildpacks(nodeURI, npmURI),
			).Build()
			Expect(err).ToNot(HaveOccurred())

			Expect(app.Start()).To(Succeed())

			body, _, err := app.HTTPGet("/")
			Expect(err).NotTo(HaveOccurred())
			Expect(body).To(ContainSubstring("Hello, World!"))

			logs, err := app.Logs()
			Expect(err).NotTo(HaveOccurred())
			Expect(logs).To(ContainSubstring("Im a baller"))
		})
	})
}
