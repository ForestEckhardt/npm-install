package integration_test

import (
	"path/filepath"
	"testing"

	"github.com/cloudfoundry/dagger"
	"github.com/sclevine/spec"

	. "github.com/onsi/gomega"
)

func testEmptyNodeModules(t *testing.T, context spec.G, it spec.S) {
	var (
		Expect = NewWithT(t).Expect
		app    *dagger.App
	)

	it.After(func() {
		Expect(app.Destroy()).To(Succeed())
	})

	context("when the node_modules directory is empty", func() {
		it.Pend("builds a working OCI image for an app that doesn't have a package-lock.json", func() {
			var err error
			app, err = dagger.NewPack(
				filepath.Join("testdata", "empty_node_modules"),
				dagger.RandomImage(),
				dagger.SetBuildpacks(nodeURI, npmURI),
			).Build()
			Expect(err).ToNot(HaveOccurred())

			Expect(app.Start()).To(Succeed())

			body, _, err := app.HTTPGet("/")
			Expect(err).NotTo(HaveOccurred())
			Expect(body).To(ContainSubstring("Hello, World!"))

			Expect(app.BuildLogs()).To(ContainSubstring("It is recommended to vendor the application's Node.js dependencies"))
		})
	})
}
